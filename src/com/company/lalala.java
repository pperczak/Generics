public class lalala<T> {
    private T first;
    private T secondl;

    public lalala(T first, T secondl) {
        this.first = first;
        this.secondl = secondl;
    }

    public lalala() {
    }

    public T getFirst() {
        return first;
    }

    public void setFirst(T first) {
        this.first = first;
    }

    public T getSecondl() {
        return secondl;
    }

    public void setSecondl(T secondl) {
        this.secondl = secondl;
    }
}
